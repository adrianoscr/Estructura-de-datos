    private void jbtnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEnviarActionPerformed
        if (jCBDestinatario.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Error, debe seleccionar almenos 1 destinatario.");

        } else {
            String destinatario = jCBDestinatario.getSelectedItem().toString();
            String mensaje = jTXTMensaje.getText();
            cliente.enviarMensaje(destinatario, mensaje);
            jTXTAChat.append("**Yo--> " + destinatario + ":**\n" + mensaje + "\n");
            jTXTMensaje.setText("");

        }
    }//GEN-LAST:event_jbtnEnviarActionPerformed

