    public void addUsuarios(String usuarioNuevo) {
        jCBDestinatario.addItem(usuarioNuevo);
    }

    public void dropUsuario(String usuario) {
        for (int i = 0; i < jCBDestinatario.getItemCount(); i++) {
            if (jCBDestinatario.getItemAt(i).toString().equals(usuario)) {
                jCBDestinatario.remove(i);
                return;

            }
        }
    }

    public void addmensaje(String usuario, String mensaje) {
        jTXTAChat.append("**" + usuario + "**\n" + mensaje + "\n");
    }

    private String[] obtenerIPPuertoNombre() {

        String[] vect = new String[3];

        //0. IP
        //1. Puerto
        //2. Nombre del chat
        vect[0] = ipDefault;
        vect[1] = puertoDefault;
        JTextField ip = new JTextField(20);
        JTextField puerto = new JTextField(20);
        JTextField usuario = new JTextField(20);
        ip.setText(ipDefault);
        puerto.setText(puertoDefault);
        usuario.setText("Usuario");

        JPanel jPanel = new JPanel();
        jPanel.setLayout(new GridLayout(3, 2));
        jPanel.add(new JLabel("Ip del server: "));
        jPanel.add(ip);
        jPanel.add(new JLabel("Puerto del Conexión: "));
        jPanel.add(puerto);
        jPanel.add(new JLabel("Nombre de Usuario "));
        jPanel.add(usuario);

        int resultado = JOptionPane.showConfirmDialog(null, jPanel, "Configuración", JOptionPane.OK_CANCEL_OPTION);

        if (resultado == JOptionPane.OK_OPTION) {
            vect[0] = ip.getText();
            vect[1] = puerto.getText();
            vect[2] = usuario.getText();
        } else {
            System.exit(0);
        }
        return vect;
    }

